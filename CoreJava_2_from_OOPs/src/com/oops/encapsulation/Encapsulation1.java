package com.oops.encapsulation;

public class Encapsulation1 {
	
	private int age =10;
	private String name ="Venu Gopal";
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
